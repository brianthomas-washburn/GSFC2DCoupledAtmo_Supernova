	SUBROUTINE CSIN_NO

        include "com2d.h"


	COMMON/NOPARAM/SMO2A(6),SMWA(6,2),SMNOA(6,2),SMO2B(6),
     *  SMWB(6,2),SMNOB(6,2),SMO2C(6),SMWC(6,2),SMNOC(6,2)

	SAVE


C INPUT PHOTODISSOCIATION CROSS-SECTIONS INFORMATION FOR NO FROM
C TABLE 1 OF MINSCHWANER AND SISKIND (JGR, 98, 20401-20412, 1993).

	WRITE(6,3104)
 3104	FORMAT(' READ XSECT_NO.DAT')

	READ(95,13)(SMO2A(I),I=1,6)
	DO 100 J11=1,2
	READ(95,13)(SMWA(I,J11),I=1,6)
	READ(95,13)(SMNOA(I,J11),I=1,6)
 100	CONTINUE

	READ(95,13)(SMO2B(I),I=1,6)
	DO 200 J11=1,2
	READ(95,13)(SMWB(I,J11),I=1,6)
	READ(95,13)(SMNOB(I,J11),I=1,6)
 200	CONTINUE

	READ(95,13)(SMO2C(I),I=1,6)
	DO 300 J11=1,2
	READ(95,13)(SMWC(I,J11),I=1,6)
	READ(95,13)(SMNOC(I,J11),I=1,6)
 300	CONTINUE

	RETURN

   13 FORMAT(1P8E10.3)

      END


